package com.learning.automation.java.learning;

public class operatorandoperand {
	
	public static void main(String[] args) {
		int A = 20;
		int B = 10;
		int Result;
		System.out.println("A=" +A +"," +"B=" +B);
		
		Result = A+B ;
		System.out.println("Addition of "  +A + " and "  +B + " = " +Result);
		
		Result = A-B ;
		System.out.println("Subtraction of "  +A + " and "  +B + " = " +Result);
		
		Result = A*B ;
		System.out.println("Multiplication of "  +A + " and "  +B + " = " +Result);
		
		Result = A/B ;
		System.out.println("Division of "  +A + " and "  +B + " = " +Result);
		
		Result = A%B ;
		System.out.println("Modulo of "  +A + " and "  +B + " = " +Result);
	}

}
