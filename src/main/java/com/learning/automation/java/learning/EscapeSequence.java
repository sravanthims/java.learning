package com.learning.automation.java.learning;

public class EscapeSequence {

	public static void main(String[] args) {

		System.out.println(" \"slash t inserts \" \t tab=5 spaces ");
		System.out.println(" \"slash b inserts \" \b back space ");
		System.out.println(" \"slash n inserts \" \n new line ");
		System.out.println(" \"slash r inserts \" \r carriage return 0 ");
		System.out.println(" \"slash f inserts \" \f formfeed in the text ");
		System.out.println(" \"slash ' inserts \" \' single quote character in the text ");
		System.out.println(" \"slash double quote inserts \" \" double quote character in the text ");
		System.out.println(" \"slash backslash inserts \" \\ backslash in the text ");

	}

}
